namespace Denpou.Enums;

public enum ENavigationBarVisibility
{
    /// <summary>
    ///     Shows it depending on the amount of items.
    /// </summary>
    Auto = 0,

    /// <summary>
    ///     Will not show it at any time.
    /// </summary>
    Never = 1,

    /// <summary>
    ///     Will show it at any time.
    /// </summary>
    Always = 2
}